#include <unistd.h>

void	ft_putnbr(int nb)
{
	unsigned int	i;
	char			digit;

	i = nb;
	if (i < 0)
	{
		write(1, "-", 1);
		i = -1;
	}
	if (i / 10)
	{
		ft_putnbr (i / 10);
	}
	digit = '0' + (i % 10);
	write(1, &digit, 1);
}
